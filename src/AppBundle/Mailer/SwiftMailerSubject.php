namespace AppBundle\Mailer;

/**
 * Class SwiftMailerSubject.
 */
class SwiftMailerSubject implements MailSubjectInterface
{
    protected $to;
    protected $from;
    protected $template;
    protected $context;

    /**
     * @return mixed
     */
    public function getTo()
    {
        return $this->to;
    }

    /**
     * @param mixed $to
     *
     * @return MailSubjectInterface
     */
    public function setTo($to)
    {
        $this->to = $to;

        return $this;
    }

    /**
     * @return mixed
     */
    public function getFrom()
    {
        return $this->from;
    }

    /**
     * @param $from
     *
     * @return MailSubjectInterface
     */
    public function setFrom($from)
    {
        $this->from = $from;

        return $this;
    }

    /**
     * @return string
     */
    public function getTemplate()
    {
        return $this->template;
    }

    /**
     * @param $template
     *
     * @return MailSubjectInterface
     */
    public function setTemplate($template)
    {
        $this->template = $template;

        return $this;
    }

    /**
     * @return array
     */
    public function getContext()
    {
        return $this->context;
    }

    /**
     * @param $context
     *
     * @return MailSubjectInterface
     */
    public function setContext($context)
    {
        $this->context = $context;
    }
}
